int	intup(int i)
{
	return(++i);
}
int main()
{
	int i[2];
	i[0] = 1;
	i[1] = 2;
	::iter(i, 2, intup);
	std::cout << i[0] << "  " << i[1] << std::endl;
	return(0);
}
